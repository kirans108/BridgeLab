package com.assignments.array;

public class PrintArrayElements {
    public static void main(String[] args) {

        int[] array = {1, 2, 3, 4, 5};


        System.out.println("Elements of the array:");

        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }
    }
}

